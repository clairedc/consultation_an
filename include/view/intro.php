        <p>Depuis la promulgation de la <a href="">loi sur la transparence de la vie publique</a>, les parlemenaires doivent déclarer leurs intérêts à la Haute Autorité pour la Transparence de la Vie Publique en charge de les contrôler et de les rendre publics afin que chaque citoyen puisse évaluer les possibles risques de confilts d'intérêts de ses représentants.</p>
